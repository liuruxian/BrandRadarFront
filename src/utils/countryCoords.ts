// ─── 辅助函数 ───────────────────────────────────────────────────────────────

/**
 * 根据 ISO Alpha-3 代码获取国家中心点经纬度
 */
export function getCountryCentroid(code: string): [number, number] | null {
  return COUNTRY_CENTROIDS[code] ?? null
}

/**
 * 根据 ISO Alpha-3 代码获取地图 numeric ID
 */
export function getCountryNumericId(code: string): string | null {
  return ISO3_TO_NUMERIC[code] ?? null
}

/**
 * 根据 Alpha-3 code 获取完整国家信息
 */
export function getCountryInfo(code: string): {
  numericId: string | null
  centroid: [number, number] | null
} {
  return {
    numericId: ISO3_TO_NUMERIC[code] ?? null,
    centroid: COUNTRY_CENTROIDS[code] ?? null,
  }
}
